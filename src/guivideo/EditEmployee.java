    private void jBSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSaveActionPerformed
       if(jTFEmployeeName.getText().isEmpty()||
              jTFEmployeeSurname.getText().isEmpty()||
              jTFEmployeeStaff.getText().isEmpty()||
              jCBEmployee.getSelectedItem()==null)
      {
          JOptionPane.showMessageDialog(null, "You have to enter all fields!");
      }
      else{
           int selectedIndex=jCBEmployee.getSelectedIndex();
           employees.get(selectedIndex).setName(jTFEmployeeName.getText().trim());
           employees.get(selectedIndex).setSurname(jTFEmployeeSurname.getText().trim());
           employees.get(selectedIndex).setStaffNr(Integer.parseInt(jTFEmployeeStaff.getText().trim()));
          Job job=jobs.get(jCBJob.getSelectedIndex());
           employees.get(selectedIndex).setJob(job);
           
           
           saveEmployeesToFile();
           
           
      } 
    }//GEN-LAST:event_jBSaveActionPerformed

