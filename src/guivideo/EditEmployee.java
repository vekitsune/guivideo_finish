    private void jCBEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBEmployeeActionPerformed
      int selectedIndex=jCBEmployee.getSelectedIndex();
      
      jTFEmployeeName.setText(employees.get(selectedIndex).getName());
      jTFEmployeeSurname.setText(employees.get(selectedIndex).getSurname());
      
     
      jTFEmployeeStaff.setText(employees.get(selectedIndex).getStaffNr()+"");
      
      Job job =employees.get(selectedIndex).getJob();
      
      
      int index=0;
        for (int i = 0; i < jobs.size(); i++) {
            if(jobs.get(i).equals(job)){
                index=i;
                break;
            }
        }
        
        jCBJob.setSelectedIndex(index);
        
    }//GEN-LAST:event_jCBEmployeeActionPerformed

