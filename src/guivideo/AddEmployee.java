public void saveEmployeesToFile(){
    
    try{
        FileOutputStream file=new FileOutputStream("Employees.dat");
        ObjectOutputStream outputFile=new ObjectOutputStream(file);
        for (int i = 0; i < employees.size(); i++) {
            
            outputFile.writeObject(employees.get(i));
        }
        outputFile.close();
        
        JOptionPane.showMessageDialog(null, "Succesfully saved!");
        this.dispose();
    }catch(IOException e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}

    public void populateArrayList() {
         try{
            FileInputStream file=new FileInputStream("Jobs.dat");
            ObjectInputStream inputFile=new ObjectInputStream(file);
            
            boolean endOfFile=false;
            while(!endOfFile){
                try{
                    jobs.add((Job) inputFile.readObject());
                }catch(EOFException e){
                    endOfFile=true;
                    
                }catch(Exception f){
                    JOptionPane.showMessageDialog(null, "Gond  a jobs.dat fileban");
                }
            }inputFile.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "ioexeption gond a jobs-dat fileban");
        }
        try {
            FileInputStream file2 = new FileInputStream("Employees.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);

            boolean endOfFile2 = false;
            while (!endOfFile2) {
                try {
                    employees.add((Employee) inputFile2.readObject());
                } catch (EOFException e) {
                    endOfFile2 = true;

                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null,"itt van a gond?");
                }
            }
            inputFile2.close();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "ioException employees.dat nÃ©l");
        }
    }


